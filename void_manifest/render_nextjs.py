"""Manifest Engine — deterministic compiler from ArchitecturalGraph to code files.

This is Step 3 of the Puranic creation model: the Conscious Void (ArchitecturalGraph)
is rendered into working code files. No LLM. No generation. Pure compilation.

Given the same graph, it produces the same code. Every time. Zero hallucination.
"""
from __future__ import annotations

import os
from typing import Dict, List

from tools.void_manifest.arch_schema import (
    ArchitecturalGraph, EntityDef, ComponentDef, RouteDef, PageDef,
    FieldType, RelationType, ComponentType, AuthType, HttpMethod,
    _to_snake, _to_camel,
)


def render(graph: ArchitecturalGraph, output_dir: str) -> Dict[str, str]:
    """Compile the Architectural Graph into code files.

    Returns {relative_path: file_content} dict. Does NOT write to disk —
    call write_files() to persist.

    Target stack: Next.js 14+ (App Router) + TypeScript + Prisma + Tailwind.
    """
    files: Dict[str, str] = {}

    # ── Database schema ──────────────────────────────────────────────────
    if graph.app.stack == "nextjs":
        files["prisma/schema.prisma"] = _render_prisma_schema(graph)
        files["src/lib/prisma.ts"] = _render_prisma_client()

    # ── TypeScript types ─────────────────────────────────────────────────
    files["src/types/index.ts"] = _render_types(graph)

    # ── API routes ───────────────────────────────────────────────────────
    for route in graph.routes:
        route_path = _route_to_filepath(route.path, route.method)
        files[f"src/app/api{route_path}/route.ts"] = _render_route(route, graph)

    # ── Auth middleware ──────────────────────────────────────────────────
    if graph.auth.type != AuthType.NONE:
        files["src/lib/auth.ts"] = _render_auth(graph)

    # ── Components ───────────────────────────────────────────────────────
    for comp in graph.components:
        if comp.type not in (ComponentType.PAGE, ComponentType.LAYOUT):
            files[f"src/components/{comp.name}.tsx"] = _render_component(comp, graph)

    # ── Pages ────────────────────────────────────────────────────────────
    for page in graph.pages:
        page_dir = _page_to_dir(page.path)
        files[f"src/app{page_dir}/page.tsx"] = _render_page(page, graph)

    # ── Root layout ─────────────────────────────────────────────────────
    files["src/app/layout.tsx"] = _render_root_layout(graph)

    # ── Package.json ────────────────────────────────────────────────────
    files["package.json"] = _render_package_json(graph)

    # ── Environment ──────────────────────────────────────────────────────
    files[".env.local.example"] = _render_env(graph)

    # ── README ───────────────────────────────────────────────────────────
    files["README.md"] = _render_readme(graph)

    return files


def write_files(files: Dict[str, str], output_dir: str) -> List[str]:
    """Write rendered files to disk. Creates directories as needed."""
    written = []
    for rel_path, content in files.items():
        full_path = os.path.join(output_dir, rel_path)
        os.makedirs(os.path.dirname(full_path), exist_ok=True)
        with open(full_path, "w") as f:
            f.write(content)
        written.append(rel_path)
    return written


