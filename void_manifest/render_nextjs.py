# ─── Helpers ──────────────────────────────────────────────────────────────────

def _field_to_prisma(ftype: FieldType) -> str:
    MAP = {
        FieldType.STRING: "String",
        FieldType.INTEGER: "Int",
        FieldType.FLOAT: "Float",
        FieldType.BOOLEAN: "Boolean",
        FieldType.DATETIME: "DateTime",
        FieldType.TEXT: "String",
        FieldType.EMAIL: "String",
        FieldType.URL: "String",
        FieldType.JSON: "Json",
    }
    return MAP.get(ftype, "String")


def _field_to_typescript(ftype: FieldType) -> str:
    MAP = {
        FieldType.STRING: "string",
        FieldType.INTEGER: "number",
        FieldType.FLOAT: "number",
        FieldType.BOOLEAN: "boolean",
        FieldType.DATETIME: "Date",
        FieldType.TEXT: "string",
        FieldType.EMAIL: "string",
        FieldType.URL: "string",
        FieldType.JSON: "Record<string, any>",
    }
    return MAP.get(ftype, "string")


def _json_schema_to_ts(schema: dict) -> str:
    jtype = schema.get("type", "string")
    MAP = {
        "string": "string", "integer": "number", "number": "number",
        "boolean": "boolean", "array": "any[]", "object": "Record<string, any>",
    }
    return MAP.get(jtype, "any")


def _find_entity(graph: ArchitecturalGraph, name: str) -> EntityDef:
    for e in graph.entities:
        if e.name == name:
            return e
    return None


def _find_component(graph: ArchitecturalGraph, name: str) -> ComponentDef:
    for c in graph.components:
        if c.name == name:
            return c
    return None


def _find_id_field(entity: EntityDef):
    if not entity:
        return None
    for f in entity.fields:
        if f.name == "id":
            return f
    return None


def _route_to_filepath(path: str, method: HttpMethod) -> str:
    """Convert route path to Next.js App Router file path.

    /api/users → /users  (the 'api' prefix is handled by src/app/api/ directory)
    /api/users/:id → /users/[id]
    """
    clean = path.rstrip("/")
    # Strip leading /api since it's implicit in the src/app/api/ directory
    if clean.startswith("/api/"):
        clean = clean[4:]  # remove /api, keep the rest
    elif clean == "/api":
        clean = ""
    # Convert :param → [param] for Next.js dynamic routes
    parts = []
    for p in clean.split("/"):
        if p.startswith(":"):
            parts.append(f"[{p[1:]}]")
        else:
            parts.append(p)
    return "/".join(parts)


def _route_to_type_name(route: RouteDef) -> str:
    """Generate type name from route, e.g. /api/users POST → CreateUser"""
    parts = route.path.strip("/").split("/")
    entity = parts[-1] if parts[-1] and not parts[-1].startswith(":") else (parts[-2] if len(parts) > 1 else "Item")
    entity_singular = entity.rstrip("s") if entity.endswith("s") else entity
    entity_pascal = _capitalize(entity_singular)

    VERB_MAP = {
        HttpMethod.GET: "Get",
        HttpMethod.POST: "Create",
        HttpMethod.PUT: "Update",
        HttpMethod.PATCH: "Update",
        HttpMethod.DELETE: "Delete",
    }
    return f"{VERB_MAP.get(route.method, 'Handle')}{entity_pascal}"


def _guess_entity_from_route(route: RouteDef, graph: ArchitecturalGraph) -> EntityDef:
    """Guess which entity a route operates on from its path."""
    if route.queries_entities:
        return _find_entity(graph, route.queries_entities[0])
    parts = [p for p in route.path.strip("/").split("/") if not p.startswith(":") and p not in ("api",)]
    if parts:
        entity_name = _capitalize(parts[-1].rstrip("s"))
        entity = _find_entity(graph, entity_name)
        if entity:
            return entity
    return None


def _page_to_dir(path: str) -> str:
    """Convert page path to Next.js directory, e.g. /dashboard → /dashboard, /sutras/:id → /sutras/[id]"""
    clean = path.rstrip("/")
    parts = []
    for p in clean.split("/"):
        if p.startswith(":"):
            parts.append(f"[{p[1:]}]")
        else:
            parts.append(p)
    return "/".join(parts)


def _page_to_component_name(page: PageDef) -> str:
    """Generate component name from page path, e.g. /sutras/:id → SutraDetailPage"""
    path = page.path.strip("/")
    # Strip dynamic params (:id → id) and take the meaningful segments
    parts = []
    for p in path.replace("-", " ").replace("/", " ").split():
        if p.startswith(":"):
            parts.append(p[1:])  # strip colon
        else:
            parts.append(p)
    if len(parts) >= 2 and parts[-1] in ("id", "slug", "key"):
        # /sutras/:id → SutraDetail, /posts/:slug → PostDetail
        name = "".join(w.capitalize() for w in parts[:-1]) + "Detail"
    else:
        name = "".join(w.capitalize() for w in parts)
    return name + "Page"


def _format_default(val) -> str:
    if val is None:
        return "undefined"
    if isinstance(val, bool):
        return str(val).lower()
    if isinstance(val, str):
        return f'"{val}"'
    return str(val)


def _capitalize(s: str) -> str:
    if not s:
        return s
    return s[0].upper() + s[1:]


def _normalize_ts_type(ptype: str) -> str:
    """Normalize LLM-produced type strings to valid TypeScript types."""
    MAP = {
        "array": "any[]",
        "function": "() => void",
        "object": "Record<string, any>",
        "number": "number",
        "string": "string",
        "boolean": "boolean",
        "date": "Date",
        "any": "any",
        "void": "void",
    }
    ptype_lower = ptype.lower().strip()
    if ptype_lower in MAP:
        return MAP[ptype_lower]
    # If it ends with [], it's an array of some type
    if ptype.endswith("[]"):
        base = ptype[:-2]
        normalized_base = MAP.get(base.lower(), base)
        return f"{normalized_base}[]"
    return ptype


def _pluralize(name: str) -> str:
    """Naive pluralization — good enough for entity names."""
    if name.endswith("s") or name.endswith("x") or name.endswith("z") or name.endswith("ch") or name.endswith("sh"):
        return name + "es"
    if name.endswith("y") and len(name) > 1 and name[-2] not in "aeiou":
        return name[:-1] + "ies"
    return name + "s"
