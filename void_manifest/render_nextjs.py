def _render_prisma_schema(graph: ArchitecturalGraph) -> str:
    """Generate Prisma schema from entity definitions."""
    lines = [
        "// Generated by Void Manifest Engine",
        "// DO NOT EDIT BY HAND — regenerate from the Architectural Graph",
        "",
        'datasource db {',
        '  provider = "postgresql"',
        '  url      = env("DATABASE_URL")',
        "}",
        "",
        'generator client {',
        '  provider = "prisma-client-js"',
        "}",
        "",
    ]

    for e in graph.entities:
        lines.append(f"model {e.name} {{")
        existing_fields = {f.name for f in e.fields}
        for f in e.fields:
            prisma_type = _field_to_prisma(f.type)
            attrs = ""
            if f.name == "id" and f.type == FieldType.INTEGER:
                attrs = " @id @default(autoincrement())"
            elif f.name == "id" and f.type == FieldType.STRING:
                attrs = ' @id @default(uuid())'
            if f.unique and "id" not in attrs:
                attrs += " @unique"
            if f.type == FieldType.EMAIL:
                attrs += " @unique" if "unique" not in attrs else ""
            required = "" if f.required else "?"
            default_str = ""
            if f.default is not None:
                if isinstance(f.default, str):
                    default_str = f' @default("{f.default}")'
                elif isinstance(f.default, bool):
                    default_str = f" @default({str(f.default).lower()})"
                else:
                    default_str = f" @default({f.default})"
            lines.append(f"  {f.name} {prisma_type}{required}{default_str}{attrs}")

        # Relations — only add FK fields that don't already exist
        for r in e.relations:
            if r.type == RelationType.BELONGS_TO:
                fk = r.foreign_key or f"{_to_snake(r.target)}_id"
                if fk not in existing_fields:
                    target = _find_entity(graph, r.target)
                    id_field = _find_id_field(target)
                    fk_type = _field_to_prisma(id_field.type) if id_field else "Int"
                    lines.append(f"  {fk} {fk_type}")
                lines.append(f"  {_to_camel(r.target)} {r.target} @relation(fields: [{fk}], references: [id])")
            elif r.type == RelationType.HAS_MANY:
                plural = _pluralize(r.target)
                lines.append(f"  {_to_camel(plural)} {r.target}[]")
            elif r.type == RelationType.HAS_ONE:
                lines.append(f"  {_to_camel(r.target)} {r.target}?")

        # Timestamps — only add if the entity doesn't define them
        if "createdAt" not in existing_fields:
            lines.append("  createdAt DateTime @default(now())")
        if "updatedAt" not in existing_fields:
            lines.append("  updatedAt DateTime @updatedAt")

        lines.append("}")
        lines.append("")

    return "\n".join(lines)


def _render_prisma_client() -> str:
    return """import { PrismaClient } from '@prisma/client'

const globalForPrisma = globalThis as unknown as { prisma: PrismaClient }

export const prisma = globalForPrisma.prisma || new PrismaClient()

if (process.env.NODE_ENV !== 'production') globalForPrisma.prisma = prisma
"""


def _render_types(graph: ArchitecturalGraph) -> str:
    """Generate TypeScript types from entity definitions."""
    lines = [
        "// Generated by Void Manifest Engine",
        "// DO NOT EDIT BY HAND",
        "",
    ]
    for e in graph.entities:
        lines.append(f"export interface {e.name} {{")
        for f in e.fields:
            ts_type = _field_to_typescript(f.type)
            optional = "" if f.required else "?"
            lines.append(f"  {f.name}{optional}: {ts_type}")
        # Include relation fields
        for r in e.relations:
            if r.type == RelationType.BELONGS_TO:
                fk = r.foreign_key or f"{_to_snake(r.target)}_id"
                lines.append(f"  {fk}: string")
                lines.append(f"  {_to_camel(r.target)}?: {r.target}")
            elif r.type == RelationType.HAS_MANY:
                lines.append(f"  {_to_camel(r.target)}s?: {r.target}[]")
            elif r.type == RelationType.HAS_ONE:
                lines.append(f"  {_to_camel(r.target)}?: {r.target}")
        lines.append(f"  createdAt: Date")
        lines.append(f"  updatedAt: Date")
        lines.append("}")
        lines.append("")

    # API types
    lines.append("// API request/response types")
    for route in graph.routes:
        if route.input_schema:
            type_name = _route_to_type_name(route) + "Input"
            lines.append(f"export interface {type_name} {{")
            for field_name, field_info in route.input_schema.get("properties", {}).items():
                ts_type = _json_schema_to_ts(field_info)
                required_fields = route.input_schema.get("required", [])
                optional = "" if field_name in required_fields else "?"
                lines.append(f"  {field_name}{optional}: {ts_type}")
            lines.append("}")
            lines.append("")

    return "\n".join(lines)


def _render_route(route: RouteDef, graph: ArchitecturalGraph) -> str:
    """Generate a Next.js App Router API route handler."""
    method_lower = route.method.value.lower()
    type_name = _route_to_type_name(route)

    lines = [
        "// Generated by Void Manifest Engine",
        f"// {route.description}",
        "",
        "import { NextRequest, NextResponse } from 'next/server'",
    ]
    if graph.auth.type != AuthType.NONE and route.auth_required:
        lines.append("import { authenticate } from '@/lib/auth'")
    lines.append("import { prisma } from '@/lib/prisma'")
    lines.append("")

    has_input = bool(route.input_schema.get("properties"))
    if has_input:
        lines.append(f"import type {{ {type_name}Input }} from '@/types'")
        lines.append("")

    # Handler function
    lines.append(f"export async function {method_lower}(request: NextRequest) {{")
    if route.auth_required and graph.auth.type != AuthType.NONE:
        lines.append("  const user = await authenticate(request)")
        lines.append("  if (!user) {")
        lines.append("    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })")
        lines.append("  }")
        lines.append("")

    if has_input:
        lines.append("  const body = await request.json()")
        lines.append("  // TODO: Add Zod validation for input schema")
        lines.append("")

    # Handler logic — rendered as structured comments guiding the dev
    if route.handler_logic:
        lines.append(f"  // {route.handler_logic}")
        lines.append("")

    # Generate CRUD boilerplate based on pattern matching
    entity = _guess_entity_from_route(route, graph)
    if entity:
        if route.method == HttpMethod.GET and "/:" not in route.path:
            lines.append(f"  const items = await prisma.{_to_camel(entity.name)}.findMany()")
            lines.append(f"  return NextResponse.json(items)")
        elif route.method == HttpMethod.GET:
            lines.append(f"  const id = request.nextUrl.pathname.split('/').pop()")
            lines.append(f"  const item = await prisma.{_to_camel(entity.name)}.findUnique({{ where: {{ id }} }})")
            lines.append(f"  if (!item) return NextResponse.json({{ error: 'Not found' }}, {{ status: 404 }})")
            lines.append(f"  return NextResponse.json(item)")
        elif route.method == HttpMethod.POST:
            lines.append(f"  const item = await prisma.{_to_camel(entity.name)}.create({{ data: body }})")
            lines.append(f"  return NextResponse.json(item, {{ status: 201 }})")
        elif route.method == HttpMethod.PUT or route.method == HttpMethod.PATCH:
            lines.append(f"  const id = request.nextUrl.pathname.split('/').pop()")
            lines.append(f"  const item = await prisma.{_to_camel(entity.name)}.update({{ where: {{ id }}, data: body }})")
            lines.append(f"  return NextResponse.json(item)")
        elif route.method == HttpMethod.DELETE:
            lines.append(f"  const id = request.nextUrl.pathname.split('/').pop()")
            lines.append(f"  await prisma.{_to_camel(entity.name)}.delete({{ where: {{ id }} }})")
            lines.append(f"  return NextResponse.json({{ success: true }})")
    else:
        lines.append(f"  // Route: {route.method.value} {route.path}")
        lines.append(f"  // Handler logic: {route.handler_logic}")
        lines.append(f"  return NextResponse.json({{ message: 'Implement {route.handler_logic[:50]}...' }})")

    lines.append("}")
    return "\n".join(lines)


def _render_auth(graph: ArchitecturalGraph) -> str:
    """Generate auth middleware."""
    return f"""// Generated by Void Manifest Engine
import {{ NextRequest }} from 'next/server'
import {{ jwtVerify }} from 'jose'

const JWT_SECRET = new TextEncoder().encode(process.env.JWT_SECRET || 'dev-secret-change-me')

export async function authenticate(request: NextRequest) {{
  const token = request.headers.get('{graph.auth.token_field}')?.replace('Bearer ', '')
  if (!token) return null
  try {{
    const {{ payload }} = await jwtVerify(token, JWT_SECRET)
    return payload as {{ sub: string; role: string }}
  }} catch {{
    return null
  }}
}}

export function requireRole(user: any, ...roles: string[]) {{
  if (!user) return false
  return roles.length === 0 || roles.includes(user.role)
}}
"""


def _render_component(comp: ComponentDef, graph: ArchitecturalGraph) -> str:
    """Generate a React component."""
    lines = [
        "// Generated by Void Manifest Engine",
        f"// {comp.description}",
        "",
        "'use client'",
        "",
        "import { useState } from 'react'",
        "",
    ]

    # Props type
    if comp.props:
        lines.append(f"interface {comp.name}Props {{")
        for prop in comp.props:
            ptype = _normalize_ts_type(str(prop.get("type", "any")))
            optional = "" if prop.get("required", False) else "?"
            lines.append(f"  {prop['name']}{optional}: {ptype}")
        lines.append("}")
        lines.append("")

    # Component
    props_destructure = ""
    if comp.props:
        prop_names = [p["name"] for p in comp.props]
        props_destructure = "{ " + ", ".join(prop_names) + " }"

    if comp.props:
        lines.append(f"export default function {comp.name}({props_destructure}: {comp.name}Props) {{")
    else:
        lines.append(f"export default function {comp.name}() {{")

    # State
    for s in comp.state:
        default_val = _format_default(s.get("default"))
        stype = _normalize_ts_type(str(s.get("type", "any")))
        lines.append(f"  const [{s['name']}, set{_capitalize(s['name'])}] = useState<{stype}>({default_val})")

    if comp.type == ComponentType.FORM:
        lines.append("")
        lines.append(f"  const handleSubmit = async (e: React.FormEvent) => {{")
        lines.append("    e.preventDefault()")
        lines.append("    // TODO: Wire to API route")
        lines.append("  }}")
        lines.append("")
        lines.append("  return (")
        lines.append(f'    <form onSubmit={{handleSubmit}} className="space-y-4">')
        if comp.props:
            for prop in comp.props:
                lines.append(f"      <div>")
                lines.append(f'        <label className="block text-sm font-medium">{prop["name"]}</label>')
                input_type = "email" if "email" in prop["name"].lower() else "text"
                lines.append(f'        <input type="{input_type}" className="mt-1 block w-full rounded border p-2" />')
                lines.append(f"      </div>")
        lines.append(f'      <button type="submit" className="rounded bg-blue-600 px-4 py-2 text-white">Submit</button>')
        lines.append(f"    </form>")
        lines.append("  )")

    elif comp.type == ComponentType.CARD:
        lines.append("  return (")
        lines.append(f'    <div className="rounded-lg border p-4 shadow-sm">')
        lines.append(f"      {{/* {comp.description} */}}")
        if comp.props:
            for prop in comp.props:
                lines.append(f"      {{/* {prop['name']}: {{{prop['name']}}} */}}")
        lines.append(f"    </div>")
        lines.append("  )")

    elif comp.type == ComponentType.LIST:
        lines.append("  return (")
        lines.append(f'    <div className="space-y-2">')
        lines.append(f"      {{/* Iterate over items */}}")
        lines.append(f"    </div>")
        lines.append("  )")

    else:
        lines.append("  return (")
        lines.append(f'    <div>')
        lines.append(f"      {{/* {comp.name}: {comp.description} */}}")
        lines.append(f"    </div>")
        lines.append("  )")

    lines.append("}")
    return "\n".join(lines)


def _render_page(page: PageDef, graph: ArchitecturalGraph) -> str:
    """Generate a Next.js page."""
    lines = [
        "// Generated by Void Manifest Engine",
        f"// {page.title}",
        "",
    ]
    if page.auth_required:
        lines.append("import { redirect } from 'next/navigation'")
        lines.append("import { authenticate } from '@/lib/auth'")
        lines.append("")

    # Import components used on this page
    for cname in page.components:
        comp = _find_component(graph, cname)
        if comp and comp.type not in (ComponentType.PAGE, ComponentType.LAYOUT):
            lines.append(f"import {cname} from '@/components/{cname}'")
    lines.append("")

    if page.auth_required:
        lines.append(f"export default async function {_page_to_component_name(page)}() {{")
        lines.append("  const user = await authenticate(/* request */)")
        lines.append("  if (!user) redirect('/login')")
        lines.append("")
    else:
        lines.append(f"export default function {_page_to_component_name(page)}() {{")

    lines.append("  return (")
    lines.append(f'    <main className="container mx-auto p-4">')
    lines.append(f"      <h1 className=\"text-2xl font-bold\">{page.title}</h1>")

    if page.description:
        lines.append(f"      <p className=\"text-gray-600 mt-2\">{page.description}</p>")

    # Render components
    for cname in page.components:
        lines.append(f"      <{cname} />")

    lines.append(f"    </main>")
    lines.append("  )")
    lines.append("}")
    return "\n".join(lines)


